public class Car {
    String mark, model, color;
    int releaseYear, price, id;

    Car(String mark, String model, int releaseYear, String color, int price, int id) {
        this.mark = mark;
        this.model = model;
        this.releaseYear = releaseYear;
        this.color = color;
        this.price = price;
        this.id = id;
    }
}
